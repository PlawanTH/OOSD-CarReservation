        if( n == JOptionPane.YES_OPTION ){
            connectDB();
            String sql_delete = "DELETE FROM CAR_Reserve WHERE ReserveID = "+id;
            System.out.println(db.executeQuery(sql_delete));
            String sql_update = "UPDATE CAR_Car SET Status='Available' WHERE ID ='"+ca_id+"'";
            System.out.println(db.executeQuery(sql_update));
            System.out.println(db.disconnect());
            dispose();
        }
