    public void setInitText(){
        reserve_id.setText((String) reserving_detail.get(0).get("ReserveID"));
        
        String sql_customer = "SELECT * FROM CAR_Customer WHERE ID ="+Integer.parseInt((String) reserving_detail.get(0).get("CustomerID"));
        HashMap customer = db.queryRow(sql_customer);
        cu_id = Integer.parseInt((String) reserving_detail.get(0).get("CustomerID"));
        cu_na = (String) customer.get("Name");
        cu_la = (String) customer.get("Lastname");
        cu_age = (String) customer.get("Age");
        cu_addr = (String) customer.get("Address");
        cu_city = (String) customer.get("City");
        cu_country = (String) customer.get("Country");
        cu_email = (String) customer.get("Email");
        cu_phone = (String) customer.get("PhoneNumber");
        
        customer_name.setText(cu_na);
        customer_lastname.setText(cu_la);
        age.setText(cu_age);
        address.setText(cu_addr);
        city.setText(cu_city);
        country.setText(cu_country);
        email.setText(cu_email);
        phonenumber.setText(cu_phone);
        
        String sql_car = "SELECT * FROM CAR_Car WHERE ID ='"+((String) reserving_detail.get(0).get("CarID"))+"'";
        HashMap car = db.queryRow(sql_car);
        ca_id = (String) reserving_detail.get(0).get("CarID");
        
        car_id.setText(ca_id);
        car_brand.setText((String) car.get("brand"));
        car_series.setText((String) car.get("series"));
        car_year.setText((String) car.get("year"));
        license.setText((String) car.get("LicensePage"));
        
        pick = (String) reserving_detail.get(0).get("PickUp_Date");
        ret = (String) reserving_detail.get(0).get("Return_Date");
        loc = (String) reserving_detail.get(0).get("Location");
        mile = Integer.parseInt((String) reserving_detail.get(0).get("Mileage"));
        st = (String) reserving_detail.get(0).get("Status");
        
        pickup_date.setText(pick);
        return_date.setText(ret);
        location.setText(loc);
        mileage.setText(""+mile);
        status.setSelectedItem(st);
    }

